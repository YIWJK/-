void menu()
{
	printf("========================================================\n");
	printf("=           <1>start            <0>exit                =\n");
	printf("========================================================\n");
}

//游戏的整个算法实现
void game()
{
	//数组--存放走出的棋盘信息
	char board[ROW][COL] = { 0 };//希望棋盘空格
	//初始化棋盘
	InitBoard(board, ROW, COL);
	//打印棋盘
	DisplayBoard(board, ROW, COL);
	//下棋
	char ret = 0;
	while (1)
	{
		
		//玩家下棋
		PlayerMove(board,ROW,COL);
		DisplayBoard(board, ROW, COL);
		//判断玩家是否赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
		//电脑下棋
		ComputerMove(board,ROW,COL);
		DisplayBoard(board, ROW, COL);
		//判断电脑是否赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
	}
	if (ret == '*')
	{
		printf("You Win!\n");
	}
	else if (ret == '#')
	{
		printf("You Lost!\n");
	}
	else
	{
		printf("平局\n");
	}
}

void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));//时间戳作为随机数的初始值，头文件<stdlib.h>、<time.h>
	do
	{
		menu();
		printf("请选择：>");
		scanf("%d", &input);
		switch (input)
		{
			case 1:
				printf("开始游戏\n");
				game();
				break;
			case 0:
				printf("退出游戏\n");
				break;
			default:
				printf("选择错误，请重新选择\n");
				break;

		}
	} while (input);
}

int main()
{
	test();
	return 0;
}